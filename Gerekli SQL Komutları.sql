use Kuruyemiş
CREATE TABLE Tedarikciler(
Tedarikci_ID int PRIMARY KEY NOT NULL,
Tedarikci_Adi varchar(20),
Tedarikci_Adres varchar(500),
Telefon varchar(15),
Telefon2 varchar(15)
);

CREATE TABLE Subeler(
Sube_ID int PRIMARY KEY NOT NULL,
Tedarikci_ID int,
Sube_Adi varchar(30),
Sehir varchar(30), 
Ilce varchar(30), 
Semt varchar(30), 
Acik_Adres varchar(500),
CONSTRAINT fk_Subeler FOREIGN KEY (Tedarikci_ID) REFERENCES Tedarikciler(Tedarikci_ID)
);

CREATE TABLE KampanyaVeIndirimler(
Kampanya_ID int PRIMARY KEY,
Sube_ID int,
Kampanya_Adi varchar(40),
Kampanya_Baslama_Tarihi DATE,
Kampanya_Bitis_Tarihi DATE,
Indirim_Orani float,
CONSTRAINT fk_KampanyaVeIndirimler FOREIGN KEY (Sube_ID) REFERENCES Subeler(Sube_ID)
);

CREATE TABLE Kategoriler(
Kategori_ID int PRIMARY KEY,
Kategori_Adi varchar(25)
)

CREATE TABLE Urunler(
Urun_Kodu int PRIMARY KEY NOT NULL,
Tedarikci_kodu int,
Kategori_ID int,
Urun_Adi varchar(40),
Fiyat float,
CONSTRAINT fk_Urunler FOREIGN KEY (Tedarikci_Kodu) REFERENCES Tedarikciler(Tedarikci_ID),
CONSTRAINT fk_Urunlerr FOREIGN KEY (Kategori_ID) REFERENCES Kategoriler(Kategori_ID),
);

CREATE TABLE Stoklar(
Islem_Kodu int PRIMARY KEY,
Urun_Kodu int,
Sube_ID int,
Miktar float,
Tarih DATETIME,
Islem_Turu varchar(1),
CONSTRAINT fk_Stoklar FOREIGN KEY (Urun_Kodu) REFERENCES Urunler(Urun_Kodu),
CONSTRAINT fk_Stoklarr FOREIGN KEY (Sube_ID) REFERENCES Subeler(Sube_ID)
);


CREATE TABLE Musteri_Bilgileri(
Musteri_ID int PRIMARY KEY IDENTITY (1,1) NOT NULL, /*identity otomatik primary key atar*/
Ad varchar(15) not null,
Soyad varchar(15) not null,
KullaniciAdi varchar(30) not null,
Sifre varchar(15) not null,
Email varchar(40) not null
);

CREATE TABLE Adres_Bilgileri(
Adres_no int primary key identity NOT NULL,
M_ID int,
Telefon varchar(10) not null,
Ulke varchar(50) not null,
Sehir varchar(50) not null,
Ilce varchar(50) not null,
PostaKodu varchar(10) not null,
Acik_Adres varchar(500) not null,
CONSTRAINT fk_Adres_Bilgileri FOREIGN KEY (M_ID) REFERENCES Musteri_Bilgileri(Musteri_ID)
);

CREATE TABLE Siparisler(
Siparis_ID int PRIMARY KEY NOT NULL,
M_ID int,
Siparis_Tarihi date,
Toplam_Tutar float,
CONSTRAINT fk_Siparisler FOREIGN KEY (M_ID) REFERENCES Musteri_Bilgileri(Musteri_ID),
);


CREATE TABLE SiparisDetaylari(
Siparis_ID int,
Urun_Kodu int,
Birim_Fiyati float,
MiktarKG int,
CONSTRAINT fk_SiparisD FOREIGN KEY (Siparis_ID) REFERENCES Siparisler(Siparis_ID),
CONSTRAINT fk_SiparisDd FOREIGN KEY (Urun_Kodu) REFERENCES Urunler(Urun_Kodu)
);

CREATE TABLE Kargo(
Kargo_Kodu int PRIMARY KEY NOT NULL,
Siparis_Kodu int,
Kargoya_Verilme_Tarihi DATETIME,
Kargo_Teslim_Tarihi DATETIME,
Durum varchar(20),
CONSTRAINT fk_Kargo FOREIGN KEY (Siparis_Kodu) REFERENCES Siparisler(Siparis_ID)
);

CREATE TABLE Odemeler(
Odeme_ID int PRIMARY KEY NOT NULL,
Siparis_ID int,
Odeme_Tarihi datetime,
Odeme_Turu varchar(20),
Odeme_Tutari float,
CONSTRAINT fk_Odemeler FOREIGN KEY (Siparis_ID) REFERENCES Siparisler(Siparis_ID)
);


CREATE TABLE Personeller(
P_ID int PRIMARY KEY NOT NULL,
Sube_ID int,
Ad varchar(20),
Soyad varchar(20),
Telefon varchar(15),
Ise_Baslama_Tarihi DATE,
Pozisyon varchar(20),
Maas float,
CONSTRAINT fk_Personeler FOREIGN KEY (Sube_ID) REFERENCES Subeler(Sube_ID)
);

CREATE TABLE Personel_Bilgileri(
Personel_ID int PRIMARY KEY IDENTITY (1,1) NOT NULL, /*identity otomatik primary key atar*/
KullaniciAdi varchar(30) not null,
Sifre varchar(15) not null,
Email varchar(40) not null,
CONSTRAINT fk_Personel_Bilgileri FOREIGN KEY (Personel_ID) REFERENCES Personeller(P_ID)
);


